// A conditional statement tells a program to execute an action depending on whether a condition is true or false.

//An if statement is a programming construct that allows you to make decisions based on certain conditions. It helps you control the flow of your program by executing different blocks of code depending on whether a given condition is true or false.
let num1 = 56;
let num2 = 45;
if (num1 > num2) {
    console.log("num1 is greater than num2");
}
//Example 1: In this example above the condition is num1 is greater than num2 and is it true so the code will be executed.

const num3 = 1000;
