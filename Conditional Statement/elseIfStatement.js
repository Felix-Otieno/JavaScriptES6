// The if/else statement executes a block of code if a specified condition is true. If the condition is false, another block of code can be executed. 
let score = 45;

if (score >= 90) {
    console.log("You are doing great");
} else if (score >= 80) {
    console.log("You are doing best");
} else if (score >= 70) {
    console.log("You are doing better");
} else if (score >= 60) {
    console.log("You are doing good");
} else if (score >= 50) {
    console.log("You are average");
} else if (score >= 40) {
    console.log("You are below average");
} else {
    console.log("You need to work hard fellah");
}