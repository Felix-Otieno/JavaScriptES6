// Arithmetic Operators use a specific symbol in order to be used within the program. Arithmetic Operators use two operands and an operator between them in order to calculate the result.
let num1 = 100;
let num2 = 50;
console.log(num1 + num2);//Addition
console.log(num1 - num2);//Subtraction
console.log(num1 * num2);//Multiplication
console.log(num1 / num2);//Division
console.log(num1 % num2);//Modulus
console.log(num1 ** num2);//Exponentiation
console.log(num1++);//Post increment
console.log(++num1);//Pre increment
console.log(num1--);//Post decrement
console.log(--num1);//Pre decrement
console.log(num2++);//Post increment
console.log(++num2);//Pre increment
console.log(num2--);//Post decrement
console.log(--num2);//Pre decrement

